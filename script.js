const lightboxClose = lightbox.querySelector(".lightbox-close");

let cringeUnlocked = false;
let toastTimer = 0;
let animationFrame = 0;
let lastFrameTime = 0;
let activeOrbitCards = [];

const pageState = {
  home: { orbitOffset: 0, gridShown: GRID_STEP },
  works: { orbitOffset: 0, gridShown: GRID_STEP },
  cringe: { orbitOffset: 0, gridShown: GRID_STEP }
};

function normalizeHash() {
  const raw = (window.location.hash || "#home").replace("#", "").trim().toLowerCase();
  if (!raw) return "home";
  if (raw === "home" || raw === "works" || raw === "cringe") return raw;
  return "home";
}

function setActiveNav(route) {
  document.querySelectorAll("[data-nav]").forEach((link) => {
    link.classList.toggle("is-active", link.dataset.nav === route);
  });
}

function showToast(message) {
  clearTimeout(toastTimer);
  toast.textContent = message;
  toast.classList.add("is-visible");
  toastTimer = window.setTimeout(() => {
    toast.classList.remove("is-visible");
  }, 3000);
}

function safeCaption(name) {
  return name
    .replace(/\.[^.]+$/, "")
    .replace(/[_@|]+/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function getLoopSlice(items, start, limit) {
  if (!items.length) return [];
  const count = Math.min(limit, items.length);
  return Array.from({ length: count }, (_, index) => items[(start + index) % items.length]);
}

function lazyImage(img) {
  if (!img.dataset.src) return;
  if ("IntersectionObserver" in window) {
    lazyObserver.observe(img);
  } else {
    img.src = img.dataset.src;
    img.removeAttribute("data-src");
  }
}

const lazyObserver = "IntersectionObserver" in window
  ? new IntersectionObserver((entries, observer) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) return;
        const img = entry.target;
        img.src = img.dataset.src;
        img.removeAttribute("data-src");
        observer.unobserve(img);
      });
    }, { rootMargin: "220px 0px" })
  : null;

function openLightbox(item) {
  lightboxImage.src = item.src;
  lightboxCaption.textContent = safeCaption(item.name);
  lightbox.classList.add("is-open");
  lightbox.setAttribute("aria-hidden", "false");
  lightboxClose.focus({ preventScroll: true });
}

function closeLightbox() {
  lightbox.classList.remove("is-open");
  lightbox.setAttribute("aria-hidden", "true");
  window.setTimeout(() => {
    if (!lightbox.classList.contains("is-open")) lightboxImage.removeAttribute("src");
  }, 180);
}

function createOrbit(items) {
  if (!items.length) {
    return `<div class="empty-state">В этой папке пока нет изображений.</div>`;
  }

  const cards = items.map((item, index) => {
    const tilt = ((index * 37) % 11) - 5;
    const angle = (360 / items.length) * index;
    return `
      <button class="orbit-card" type="button"
        data-src="${item.src}"
        data-name="${escapeHtml(item.name)}"
        data-angle="${angle}"
        data-tilt="${tilt}">
        <img src="${item.src}" alt="${escapeHtml(safeCaption(item.name))}" loading="lazy" draggable="false">
      </button>
    `;
  }).join("");

  return `
    <div class="orbit-panel">
      <div class="orbit-stage" id="orbitStage">
        <div class="orbit-core">медленный<br>орбитальный<br>коллаж</div>
        ${cards}
      </div>
    </div>
  `;
}

function createGallery(items, route) {
  if (!items.length) return "";
  const shown = Math.min(pageState[route].gridShown, items.length);
  const visibleItems = items.slice(0, shown);
  const cards = visibleItems.map((item) => `
    <button class="gallery-card" type="button" data-src="${item.src}" data-name="${escapeHtml(item.name)}" data-caption="${escapeHtml(safeCaption(item.name))}">
      <img data-src="${item.src}" alt="${escapeHtml(safeCaption(item.name))}" loading="lazy" draggable="false">
    </button>
  `).join("");

  const loadMore = shown < items.length
    ? `<div class="load-more-wrap"><button class="action-button" type="button" data-load-more="${route}">Показать ещё ${Math.min(GRID_STEP, items.length - shown)}</button></div>`
    : "";

  return `
    <section aria-labelledby="galleryTitle">
      <div class="section-head">
        <div>
          <h2 id="galleryTitle">Вся галерея</h2>
          <p>Сетка грузит изображения лениво, чтобы страница не пыталась сразу проглотить весь архив.</p>
        </div>
      </div>
      <div class="gallery-grid">${cards}</div>
      ${loadMore}
    </section>
  `;
}

function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function renderFooter(route) {
  if (route === "works") {
    footerAction.innerHTML = `
      <form class="secret-form" id="secretForm" autocomplete="off">
        <input id="secretInput" type="text" placeholder="секретное слово для скрытой страницы" aria-label="Секретное слово">
        <button type="submit">Войти</button>
      </form>
    `;
    document.querySelector("#secretForm").addEventListener("submit", (event) => {
      event.preventDefault();
      const value = document.querySelector("#secretInput").value.trim().toLowerCase();
      if (value === "кринж") {
        cringeUnlocked = true;
        window.location.hash = "#cringe";
      } else {
        showToast("Неверный ключ");
      }
    });
    return;
  }

  if (route === "cringe") {
    footerAction.innerHTML = `
      <div class="exit-box">
        <p>Секретная комната открыта только до выхода или обновления страницы.</p>
        <button class="exit-button" id="exitCringe" type="button">Выйти</button>
      </div>
    `;
    document.querySelector("#exitCringe").addEventListener("click", () => {
      cringeUnlocked = false;
      window.location.hash = "#works";
    });
    return;
  }

  footerAction.innerHTML = "";
}

function render(route) {
  if (route === "cringe" && !cringeUnlocked) {
    showToast("Доступ закрыт");
    if (window.location.hash !== "#works") {
      window.location.hash = "#works";
      return;
    }
    route = "works";
  }

  const config = ROUTES[route];
  const items = IMAGE_DATA[config.dataKey] || [];
  const orbitItems = getLoopSlice(items, pageState[route].orbitOffset, ORBIT_LIMIT);

  cancelAnimationFrame(animationFrame);
  activeOrbitCards = [];
  lastFrameTime = 0;

  setActiveNav(route === "cringe" ? "" : route);

  app.innerHTML = `
    <article class="page" data-page="${route}">
      <section class="hero">
        <p class="kicker">${config.kicker}</p>
        <h1>${config.title}</h1>
        <p class="lead">${config.lead}</p>
        <div class="stats">
          <span class="stat-pill">${items.length} изображений</span>
          <span class="stat-pill">до ${Math.min(ORBIT_LIMIT, Math.max(items.length, 0))} в коллаже</span>
          <span class="stat-pill">SPA • GitHub Pages</span>
        </div>
      </section>

      <section aria-labelledby="orbitTitle">
        <div class="section-head">
          <div>
            <h2 id="orbitTitle">Орбитальный коллаж</h2>
            <p>Фото медленно идут по эллипсу. Наведи курсор на карточку — она остановится и немного приблизится.</p>
          </div>
          <div class="actions">
            <button class="action-button" type="button" data-shuffle="${route}">Перемешать</button>
            <button class="action-button is-muted" type="button" data-next-orbit="${route}">Следующие</button>
          </div>
        </div>
        ${createOrbit(orbitItems)}
      </section>

      ${createGallery(items, route)}
    </article>
  `;

  renderFooter(route);
  bindPageEvents(route, items);
  startOrbit();
  app.focus({ preventScroll: true });
}

function bindPageEvents(route, items) {
  document.querySelectorAll(".gallery-card, .orbit-card").forEach((button) => {
    const img = button.querySelector("img");
    if (img && img.dataset.src) lazyImage(img);

    button.addEventListener("click", () => {
      openLightbox({ src: button.dataset.src, name: button.dataset.name });
    });
  });

  document.querySelectorAll("[data-load-more]").forEach((button) => {
    button.addEventListener("click", () => {
      pageState[route].gridShown += GRID_STEP;
      render(route);
      document.querySelector("#galleryTitle")?.scrollIntoView({ behavior: "smooth", block: "start" });
    });
  });

  document.querySelectorAll("[data-next-orbit]").forEach((button) => {
    button.addEventListener("click", () => {
      pageState[route].orbitOffset = (pageState[route].orbitOffset + ORBIT_LIMIT) % Math.max(items.length, 1);
      render(route);
    });
  });

  document.querySelectorAll("[data-shuffle]").forEach((button) => {
    button.addEventListener("click", () => {
      pageState[route].orbitOffset = Math.floor(Math.random() * Math.max(items.length, 1));
      render(route);
    });
  });
}

function startOrbit() {
  activeOrbitCards = Array.from(document.querySelectorAll(".orbit-card"));
  if (!activeOrbitCards.length) return;

  const step = (time) => {
    if (!lastFrameTime) lastFrameTime = time;
    const delta = time - lastFrameTime;
    lastFrameTime = time;

    const stage = document.querySelector("#orbitStage");
    if (!stage) return;

    const stageRect = stage.getBoundingClientRect();
    const sampleCard = activeOrbitCards[0];
    const cardRect = sampleCard.getBoundingClientRect();
    const cardSize = Math.max(cardRect.width || 160, 120);
    const cx = stage.clientWidth / 2;
    const cy = stage.clientHeight / 2;
    const rx = Math.max(80, (stage.clientWidth - cardSize) * 0.39);
    const ry = Math.max(64, (stage.clientHeight - cardSize) * 0.31);
    const speed = 360 / ORBIT_DURATION_MS;

    activeOrbitCards.forEach((card) => {
      const isPaused = card.matches(":hover") || card.matches(":focus-visible");
      let angle = Number(card.dataset.angle || 0);
      if (!isPaused) {
        angle = (angle + delta * speed) % 360;
        card.dataset.angle = String(angle);
      }

      const rad = angle * Math.PI / 180;
      const x = cx + Math.cos(rad) * rx;
      const y = cy + Math.sin(rad) * ry;
      const tilt = Number(card.dataset.tilt || 0);
      const scale = isPaused ? 1.1 : 1;

      card.style.transform = `translate3d(${x}px, ${y}px, 0) translate(-50%, -50%) rotate(${tilt}deg) scale(${scale})`;
      card.style.zIndex = String(10 + Math.round(y));
    });

    animationFrame = requestAnimationFrame(step);
  };

  animationFrame = requestAnimationFrame(step);
}

function route() {
  render(normalizeHash());
}

lightboxClose.addEventListener("click", closeLightbox);
lightbox.addEventListener("click", (event) => {
  if (event.target === lightbox) closeLightbox();
});
window.addEventListener("keydown", (event) => {
  if (event.key === "Escape" && lightbox.classList.contains("is-open")) closeLightbox();
});
window.addEventListener("hashchange", route);

if (!window.location.hash) {
  window.location.hash = "#home";
  route();
} else {
  route();
}
